-- 6. TRIGGER THÊM MỚI: tự tính chênh lệch kiểm kho
-- Mục đích:
-- - so_luong_chenh_lech = so_luong_thuc_te - so_luong_he_thong.
-- - ty_le_chenh_lech = chênh lệch / số lượng hệ thống.
--
-- Ảnh hưởng backend:
-- - Có ảnh hưởng nhẹ.
-- - Backend chỉ cần gửi so_luong_he_thong và so_luong_thuc_te.
-- - Nếu backend gửi sẵn so_luong_chenh_lech, DB vẫn tính lại.
-------------------------------------------------------------------------------
CREATE OR REPLACE TRIGGER trg_ctpkk_calc_chenh_lech
BEFORE INSERT OR UPDATE OF so_luong_he_thong, so_luong_thuc_te ON CHITIETPHIEUKIEMKHO
FOR EACH ROW
BEGIN
    :NEW.so_luong_chenh_lech := :NEW.so_luong_thuc_te - :NEW.so_luong_he_thong;

    IF :NEW.so_luong_he_thong > 0 THEN
        :NEW.ty_le_chenh_lech :=
            ROUND(:NEW.so_luong_chenh_lech / :NEW.so_luong_he_thong, 4);
    ELSE
        :NEW.ty_le_chenh_lech := NULL;
    END IF;
END;
/


