-- 16. FUNCTION THÊM MỚI: kiểm tra nguyên liệu dưới mức tồn tối thiểu
-- Mục đích:
-- - Trả 1 nếu tồn kho < mức tồn tối thiểu.
-- - Trả 0 nếu bình thường hoặc chưa có dòng TONKHO.
--
-- Ảnh hưởng backend:
-- - Không ảnh hưởng. Có thể dùng cho màn cảnh báo tồn thấp.
-------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION fn_duoi_muc_ton_toi_thieu (
    p_ma_kho IN NUMBER,
    p_ma_nguyen_lieu IN NUMBER
) RETURN NUMBER
AS
    v_so_luong_ton NUMBER(18,3);
    v_muc_toi_thieu NUMBER(18,3);
BEGIN
    SELECT tk.so_luong_ton, nl.muc_ton_toi_thieu
    INTO v_so_luong_ton, v_muc_toi_thieu
    FROM TONKHO tk
    JOIN NGUYENLIEU nl
        ON nl.ma_nguyen_lieu = tk.ma_nguyen_lieu
    WHERE tk.ma_kho = p_ma_kho
      AND tk.ma_nguyen_lieu = p_ma_nguyen_lieu;

    IF v_so_luong_ton < v_muc_toi_thieu THEN
        RETURN 1;
    END IF;

    RETURN 0;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN 0;
END;
/


