-- 21. PROCEDURE THÊM MỚI: báo cáo tồn thấp bằng SYS_REFCURSOR
-- Mục đích:
-- - Trả danh sách nguyên liệu có so_luong_ton < muc_ton_toi_thieu.
-- - Có thể dùng cho SQL Developer, backend hoặc demo.
--
-- Cách test SQL Developer:
-- VAR rc REFCURSOR;
-- EXEC prc_bao_cao_ton_thap(NULL, :rc);
-- PRINT rc;
--
-- Ảnh hưởng backend:
-- - Không ảnh hưởng. Chỉ đọc dữ liệu.
-------------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE prc_bao_cao_ton_thap (
    p_ma_kho IN NUMBER DEFAULT NULL,
    p_result OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN p_result FOR
        SELECT
            k.ma_kho,
            k.ten_kho,
            nl.ma_nguyen_lieu,
            nl.ten_nguyen_lieu,
            dvt.ky_hieu AS don_vi_tinh,
            tk.so_luong_ton,
            nl.muc_ton_toi_thieu,
            tk.lan_cap_nhat_cuoi
        FROM TONKHO tk
        JOIN KHO k
            ON k.ma_kho = tk.ma_kho
        JOIN NGUYENLIEU nl
            ON nl.ma_nguyen_lieu = tk.ma_nguyen_lieu
        JOIN DONVITINH dvt
            ON dvt.ma_don_vi_tinh = nl.ma_don_vi_tinh
        WHERE tk.so_luong_ton < nl.muc_ton_toi_thieu
          AND (p_ma_kho IS NULL OR tk.ma_kho = p_ma_kho)
        ORDER BY k.ten_kho, nl.ten_nguyen_lieu;
END;
