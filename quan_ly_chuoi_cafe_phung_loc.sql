-- Mục đích:
-- - Khi trang_thai_thanh_toan chuyển thành SUCCESS,
--   nếu thoi_gian_thanh_toan đang NULL thì tự set CURRENT_TIMESTAMP.
-- - Nếu hóa đơn đang PENDING thì chuyển sang PAID.
--
-- Ảnh hưởng backend:
-- - Có ảnh hưởng nhẹ.
-- - Backend cần biết: khi update thanh toán SUCCESS,
--   DB có thể tự đổi trang_thai_hoa_don thành PAID.
-------------------------------------------------------------------------------
CREATE OR REPLACE TRIGGER trg_hoadon_payment_time
BEFORE INSERT OR UPDATE OF trang_thai_thanh_toan ON HOADON
FOR EACH ROW
BEGIN
    IF :NEW.trang_thai_thanh_toan = 'SUCCESS'
       AND :NEW.thoi_gian_thanh_toan IS NULL THEN
        :NEW.thoi_gian_thanh_toan := CURRENT_TIMESTAMP;
    END IF;

    IF :NEW.trang_thai_thanh_toan = 'SUCCESS'
       AND :NEW.trang_thai_hoa_don = 'PENDING' THEN
        :NEW.trang_thai_hoa_don := 'PAID';
    END IF;
END;
/


