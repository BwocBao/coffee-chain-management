-------------------------------------------------------------------------------
-- 1. TRIGGER ĐÃ CÓ: tự cập nhật TONKHO.lan_cap_nhat_cuoi
-- Mục đích:
-- - TONKHO.lan_cap_nhat_cuoi là thời điểm dòng tồn kho được cập nhật gần nhất.
-- - DEFAULT CURRENT_TIMESTAMP chỉ chạy khi INSERT.
-- - Trigger này chạy khi UPDATE so_luong_ton.
--
-- Ảnh hưởng backend:
-- - Ít ảnh hưởng.
-- - Backend có set lan_cap_nhat_cuoi hay không cũng được, DB sẽ set lại.
-------------------------------------------------------------------------------
CREATE OR REPLACE TRIGGER trg_tonkho_update_time
BEFORE UPDATE OF so_luong_ton ON TONKHO
FOR EACH ROW
BEGIN
    :NEW.lan_cap_nhat_cuoi := CURRENT_TIMESTAMP;
END;
/


-------------------------------------------------------------------------------
-- 2. TRIGGER ĐÃ CÓ: tự cập nhật trạng thái lô hàng
-- Mục đích:
-- - Nếu số lượng còn lại <= 0 thì lô chuyển sang USED_UP.
-- - Nếu lô quá hạn thì chuyển sang EXPIRED.
-- - Nếu còn hàng và chưa hết hạn thì ACTIVE.
--
-- Lưu ý:
-- - Trigger chỉ chạy khi INSERT hoặc UPDATE lô.
-- - Nếu một lô qua hạn do thời gian trôi qua nhưng không có update nào,
--   trigger sẽ không tự chạy. Vì vậy cần procedure prc_cap_nhat_lo_het_han.
--
-- Ảnh hưởng backend:
-- - Ít ảnh hưởng.
-- - Nếu backend cố tình set trạng thái khác với logic này, trigger sẽ ghi đè.
-------------------------------------------------------------------------------
CREATE OR REPLACE TRIGGER trg_lohang_update_status
BEFORE INSERT OR UPDATE OF so_luong_con_lai, han_su_dung ON LOHANG_NGUYENLIEU
FOR EACH ROW
BEGIN
    IF :NEW.so_luong_con_lai <= 0 THEN
        :NEW.trang_thai := 'USED_UP';
    ELSIF :NEW.han_su_dung IS NOT NULL
          AND :NEW.han_su_dung < TRUNC(SYSDATE) THEN
        :NEW.trang_thai := 'EXPIRED';
    ELSE
        :NEW.trang_thai := 'ACTIVE';
    END IF;
END;
/


