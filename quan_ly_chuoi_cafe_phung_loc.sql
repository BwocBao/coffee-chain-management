-- 12. FUNCTION ĐÃ CÓ: tính tồn thực tế từ lô hàng
-- Mục đích:
-- - LOHANG_NGUYENLIEU là source of truth theo lô.
-- - TONKHO là bảng tổng hợp để truy vấn nhanh.
-- - Function này giúp đối soát TONKHO với tổng lô thật.
--
-- Ảnh hưởng backend:
-- - Không ảnh hưởng. Chỉ đọc dữ liệu.
-------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION fn_ton_tu_lo (
    p_ma_kho IN NUMBER,
    p_ma_nguyen_lieu IN NUMBER
) RETURN NUMBER
AS
    v_ton NUMBER(18,3);
BEGIN
    SELECT NVL(SUM(so_luong_con_lai), 0)
    INTO v_ton
    FROM LOHANG_NGUYENLIEU
    WHERE ma_kho = p_ma_kho
      AND ma_nguyen_lieu = p_ma_nguyen_lieu
      AND trang_thai = 'ACTIVE'
      AND so_luong_con_lai > 0
      AND (
          han_su_dung IS NULL
          OR han_su_dung >= TRUNC(SYSDATE)
      );

    RETURN v_ton;
END;
/


