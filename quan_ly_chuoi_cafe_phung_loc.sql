-- 17. PROCEDURE ĐÃ CÓ: đồng bộ TONKHO từ LOHANG_NGUYENLIEU
-- Mục đích:
-- - Dùng khi cần bảo trì/đối soát.
-- - Không chạy tự động trong nghiệp vụ nhập/xuất.
--
-- Ảnh hưởng backend:
-- - Có COMMIT nên không nên gọi giữa transaction nghiệp vụ backend.
-- - Nếu gọi, TONKHO sẽ bị ghi lại theo tổng lô ACTIVE còn hạn.
-------------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE prc_dong_bo_tonkho_tu_lo (
    p_ma_kho IN NUMBER DEFAULT NULL
)
AS
BEGIN
    MERGE INTO TONKHO tk
    USING (
        WITH pairs AS (
            SELECT ma_kho, ma_nguyen_lieu
            FROM TONKHO
            WHERE p_ma_kho IS NULL OR ma_kho = p_ma_kho

            UNION

            SELECT ma_kho, ma_nguyen_lieu
            FROM LOHANG_NGUYENLIEU
            WHERE p_ma_kho IS NULL OR ma_kho = p_ma_kho
        )
        SELECT
            p.ma_kho,
            p.ma_nguyen_lieu,
            NVL((
                SELECT SUM(lh.so_luong_con_lai)
                FROM LOHANG_NGUYENLIEU lh
                WHERE lh.ma_kho = p.ma_kho
                  AND lh.ma_nguyen_lieu = p.ma_nguyen_lieu
                  AND lh.trang_thai = 'ACTIVE'
                  AND lh.so_luong_con_lai > 0
                  AND (
                      lh.han_su_dung IS NULL
                      OR lh.han_su_dung >= TRUNC(SYSDATE)
                  )
            ), 0) AS so_luong_tu_lo
        FROM pairs p
    ) src
    ON (
        tk.ma_kho = src.ma_kho
        AND tk.ma_nguyen_lieu = src.ma_nguyen_lieu
    )
    WHEN MATCHED THEN
        UPDATE SET
            tk.so_luong_ton = src.so_luong_tu_lo,
            tk.lan_cap_nhat_cuoi = CURRENT_TIMESTAMP
    WHEN NOT MATCHED THEN
        INSERT (
            ma_kho,
            ma_nguyen_lieu,
            so_luong_ton,
            lan_cap_nhat_cuoi
        )
        VALUES (
            src.ma_kho,
            src.ma_nguyen_lieu,
            src.so_luong_tu_lo,
            CURRENT_TIMESTAMP
        );

    COMMIT;
END;
/


