-- 11. PROCEDURE ĐÃ CÓ: rà soát lô hết hạn/hết hàng
-- Mục đích:
-- - Trigger trạng thái lô chỉ chạy khi insert/update.
-- - Procedure này dùng để quét định kỳ hoặc chạy khi demo.
--
-- Ảnh hưởng backend:
-- - Không ảnh hưởng nếu chỉ chạy thủ công/bảo trì.
-- - Có COMMIT nên không nên gọi giữa một transaction nghiệp vụ backend.
-------------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE prc_cap_nhat_lo_het_han
AS
BEGIN
    UPDATE LOHANG_NGUYENLIEU
    SET trang_thai = 'EXPIRED'
    WHERE so_luong_con_lai > 0
      AND han_su_dung IS NOT NULL
      AND han_su_dung < TRUNC(SYSDATE)
      AND trang_thai <> 'EXPIRED';

    UPDATE LOHANG_NGUYENLIEU
    SET trang_thai = 'USED_UP'
    WHERE so_luong_con_lai = 0
      AND trang_thai <> 'USED_UP';

    COMMIT;
END;
/


